"""Extraer subcadenas usando slicing
(sin usar listas)."""

cadena = "Python" #Creamos la variable cadena y le damos un valor predefinido

#Creamos la subcadena de la variable creada y definimos de que caracter a que caracter utilizaremos
subcadena1 = cadena[0:2] 

# Hacemos lo mismo y para completar la cadena 
subcadena2 = cadena[2:6] 

#Hacemos que nos muestre la cadena separada en sus 2 subcadenas
print(subcadena1 + "-" + subcadena2) 